import { describe, it, expect, vi } from "vitest";
import { dispatchToAgent } from "../../src/router/agent-dispatch.js";
import type {
  ProviderModule,
  ExecutionContext,
  ExecutionResult,
  StreamEvent,
} from "@agentex/agent";
import type {
  AgentConfig,
  DispatchOptions,
  InboundMessage,
  SessionEntry,
} from "../../src/types.js";

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function makeResult(overrides: Partial<ExecutionResult> = {}): ExecutionResult {
  return {
    runId: "test-run-id",
    exitCode: 0,
    signal: null,
    timedOut: false,
    startedAt: new Date().toISOString(),
    completedAt: new Date().toISOString(),
    durationMs: 0,
    errorMessage: null,
    errorCode: null,
    costUsd: null,
    model: "claude-4",
    summary: "done",
    sessionParams: null,
    sessionDisplayId: null,
    clearSession: false,
    billingType: "api",
    ...overrides,
  };
}

function createMockProvider(
  opts?: { events?: StreamEvent[]; result?: Partial<ExecutionResult> },
): ProviderModule & { lastCtx: ExecutionContext | null } {
  const mock: ProviderModule & { lastCtx: ExecutionContext | null } = {
    type: "mock",
    lastCtx: null,
    async execute(ctx: ExecutionContext): Promise<ExecutionResult> {
      mock.lastCtx = ctx;

      // Fire any pre-configured events
      if (opts?.events) {
        for (const event of opts.events) {
          ctx.onEvent?.(event);
        }
      }

      return makeResult(opts?.result);
    },
    async testEnvironment() {
      return { providerType: "mock", status: "pass" as const, checks: [], testedAt: new Date().toISOString() };
    },
  };
  return mock;
}

function makeMsg(overrides: Partial<InboundMessage> = {}): InboundMessage {
  return {
    messageId: "msg-1",
    channel: "telegram",
    senderId: "user-42",
    senderName: "Alice",
    chatType: "direct",
    target: "bot-1",
    text: "Hello agent",
    timestamp: Date.now(),
    ...overrides,
  };
}

function makeSession(overrides: Partial<SessionEntry> = {}): SessionEntry {
  return {
    key: "ses-1",
    sessionParams: { conversationId: "conv-abc" },
    lastChannel: "telegram",
    lastRoute: { channel: "telegram", target: "bot-1" },
    lastActivityAt: Date.now(),
    ...overrides,
  };
}

function makeAgentConfig(overrides: Partial<AgentConfig> = {}): AgentConfig {
  return {
    provider: "mock",
    cwd: "/home/project",
    ...overrides,
  };
}

function makeDispatchOpts(overrides: Partial<DispatchOptions> = {}): DispatchOptions {
  return {
    msg: makeMsg(),
    session: makeSession(),
    agentConfig: makeAgentConfig(),
    provider: createMockProvider(),
    onStreamEvent: vi.fn(),
    onSystemEvent: vi.fn(),
    ...overrides,
  };
}

// ---------------------------------------------------------------------------
// Tests
// ---------------------------------------------------------------------------

describe("dispatchToAgent", () => {
  it("builds ExecutionContext correctly from inputs", async () => {
    const provider = createMockProvider();
    const opts = makeDispatchOpts({ provider });

    await dispatchToAgent(opts);

    const ctx = provider.lastCtx!;
    expect(ctx).not.toBeNull();
    // runId is now auto-generated by the provider, not set by the gateway
    expect(ctx.runId).toBeUndefined();
    expect(ctx.prompt).toBe("Hello agent");
    expect(ctx.cwd).toBe("/home/project");
    expect(ctx.sessionParams).toEqual({ conversationId: "conv-abc" });
  });

  it("maps AgentConfig fields to ProviderConfig", async () => {
    const provider = createMockProvider();
    const opts = makeDispatchOpts({
      provider,
      agentConfig: makeAgentConfig({
        model: "claude-4",
        maxTurns: 10,
        timeoutSec: 120,
        skipPermissions: true,
        skillDirs: ["/skills"],
        instructionsFile: "INSTRUCTIONS.md",
        mcpServers: [{ name: "db", command: "db-server" }],
      }),
    });

    await dispatchToAgent(opts);

    expect(provider.lastCtx!.model).toBe("claude-4");
    const config = provider.lastCtx!.config!;
    expect(config.maxTurns).toBe(10);
    expect(config.timeoutSec).toBe(120);
    expect(config.skipPermissions).toBe(true);
    expect(config.skillDirs).toEqual(["/skills"]);
    expect(config.instructionsFile).toBe("INSTRUCTIONS.md");
    expect(config.mcpServers).toEqual([{ name: "db", command: "db-server" }]);
  });

  it("omits undefined AgentConfig fields from ProviderConfig", async () => {
    const provider = createMockProvider();
    const opts = makeDispatchOpts({ provider });

    await dispatchToAgent(opts);

    const config = provider.lastCtx!.config!;
    expect(config).toEqual({});
  });

  it("extracts system-init events and calls onSystemEvent", async () => {
    const systemEvent: StreamEvent = {
      type: "system",
      subtype: "init",
      sessionId: "ses-xyz",
      model: "claude-4",
      timestamp: new Date().toISOString(),
    };
    const provider = createMockProvider({ events: [systemEvent] });
    const onSystemEvent = vi.fn();
    const opts = makeDispatchOpts({ provider, onSystemEvent });

    await dispatchToAgent(opts);

    expect(onSystemEvent).toHaveBeenCalledOnce();
    expect(onSystemEvent).toHaveBeenCalledWith("ses-xyz", "claude-4");
  });

  it("forwards ALL events to onStreamEvent", async () => {
    const events: StreamEvent[] = [
      { type: "system", subtype: "init", sessionId: "s1", model: "m1", timestamp: "t1" },
      { type: "assistant", text: "hi", timestamp: "t2" },
      { type: "result", text: "done", cost: 0.01, isError: false, timestamp: "t3" },
    ];
    const provider = createMockProvider({ events });
    const onStreamEvent = vi.fn();
    const opts = makeDispatchOpts({ provider, onStreamEvent });

    await dispatchToAgent(opts);

    expect(onStreamEvent).toHaveBeenCalledTimes(3);
    expect(onStreamEvent).toHaveBeenNthCalledWith(1, events[0]);
    expect(onStreamEvent).toHaveBeenNthCalledWith(2, events[1]);
    expect(onStreamEvent).toHaveBeenNthCalledWith(3, events[2]);
  });

  it("does not call onSystemEvent for non-init system events", async () => {
    const event: StreamEvent = {
      type: "system",
      subtype: "heartbeat",
      sessionId: null,
      model: null,
      timestamp: new Date().toISOString(),
    };
    const provider = createMockProvider({ events: [event] });
    const onSystemEvent = vi.fn();
    const onStreamEvent = vi.fn();
    const opts = makeDispatchOpts({ provider, onSystemEvent, onStreamEvent });

    await dispatchToAgent(opts);

    expect(onSystemEvent).not.toHaveBeenCalled();
    // But it should still be forwarded to onStreamEvent
    expect(onStreamEvent).toHaveBeenCalledOnce();
    expect(onStreamEvent).toHaveBeenCalledWith(event);
  });

  it("renders systemPromptTemplate and prepends to prompt", async () => {
    const provider = createMockProvider();
    const opts = makeDispatchOpts({
      provider,
      msg: makeMsg({
        channel: "slack",
        senderId: "U123",
        senderName: "Bob",
        chatType: "group",
        threadId: "T456",
        text: "deploy please",
      }),
      agentConfig: makeAgentConfig({
        systemPromptTemplate:
          "Channel: {{channel}}, Sender: {{sender.name}} ({{sender.id}}), Chat: {{chatType}}{{#if threadId}}, Thread: {{threadId}}{{/if}}",
      }),
    });

    await dispatchToAgent(opts);

    const expected =
      "Channel: slack, Sender: Bob (U123), Chat: group, Thread: T456\ndeploy please";
    expect(provider.lastCtx!.prompt).toBe(expected);
  });

  it("handles systemPromptTemplate without threadId", async () => {
    const provider = createMockProvider();
    const opts = makeDispatchOpts({
      provider,
      msg: makeMsg({
        channel: "telegram",
        senderId: "U1",
        senderName: "Eve",
        chatType: "direct",
        text: "hi",
        // no threadId
      }),
      agentConfig: makeAgentConfig({
        systemPromptTemplate:
          "From {{sender.name}}{{#if threadId}} in thread {{threadId}}{{/if}}",
      }),
    });

    await dispatchToAgent(opts);

    expect(provider.lastCtx!.prompt).toBe("From Eve\nhi");
  });

  it("passes session params through to ExecutionContext", async () => {
    const provider = createMockProvider();
    const sessionParams = { conversationId: "c1", resumeId: "r1" };
    const opts = makeDispatchOpts({
      provider,
      session: makeSession({ sessionParams }),
    });

    await dispatchToAgent(opts);

    expect(provider.lastCtx!.sessionParams).toEqual(sessionParams);
  });

  it("handles null session params", async () => {
    const provider = createMockProvider();
    const opts = makeDispatchOpts({
      provider,
      session: makeSession({ sessionParams: null }),
    });

    await dispatchToAgent(opts);

    expect(provider.lastCtx!.sessionParams).toBeNull();
  });

  it("returns the ExecutionResult from the provider", async () => {
    const expectedResult = makeResult({
      exitCode: 0,
      model: "claude-4",
      summary: "Task completed",
      sessionParams: { newConvId: "c2" },
      costUsd: 0.05,
    });
    const provider = createMockProvider({ result: expectedResult });
    const opts = makeDispatchOpts({ provider });

    const result = await dispatchToAgent(opts);

    expect(result.exitCode).toBe(0);
    expect(result.model).toBe("claude-4");
    expect(result.summary).toBe("Task completed");
    expect(result.sessionParams).toEqual({ newConvId: "c2" });
    expect(result.costUsd).toBe(0.05);
  });

  it("does not set runId — providers auto-generate it", async () => {
    const provider = createMockProvider();

    await dispatchToAgent(makeDispatchOpts({ provider }));

    expect(provider.lastCtx!.runId).toBeUndefined();
  });

  it("does not include signal on the ExecutionContext", async () => {
    const controller = new AbortController();
    const provider = createMockProvider();
    const opts = makeDispatchOpts({
      provider,
      signal: controller.signal,
    });

    await dispatchToAgent(opts);

    // The ExecutionContext shape does not have a signal field;
    // the provider handles abort internally if needed
    expect(provider.lastCtx!).not.toHaveProperty("signal");
  });
});
